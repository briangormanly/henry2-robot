package com.valleytg.robotics.bluetooth_henry.collection;

public class DoublePoint {
	public double x = 0.0;
	public double y = 0.0;
	
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	

}
